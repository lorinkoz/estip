class Command(BaseCommand):
    faculties = {}
    specialties = {}
    student_types = {
        '': None,
    }

    def get_faculty(self, name):
        if name not in self.faculties:
            self.faculties[name], created = Faculty.objects.get_or_create(name=name)
        return self.faculties[name]

    def get_specialty(self, id):
        if id not in self.specialties:
            name, faculty_name, structure = local_specialties[id]
            self.specialties[id], created = Specialty.objects.get_or_create(name=name, faculty=self.get_faculty(faculty_name), structure=structure)
        return self.specialties[id]

    def get_student_type(self, code):
        if code not in self.student_types:
            self.student_types[code], created = StudentType.objects.get_or_create(name=local_types[code])
        return self.student_types[code]

    def handle(self, *args, **options):
        lines = data.splitlines()
        for line in lines:
            line = line.strip()
            if not line: continue
            chunks = line.split('|')
            code = int(chunks[0])
            full_name = chunks[1].upper().strip()
            specialty = self.get_specialty(int(chunks[2]))
            year = int(chunks[3])
            student_type = self.get_student_type(chunks[4])
            name_chunks = [x for x in [x.strip() for x in full_name.split(' ')] if x]
            if len(name_chunks) in [3, 4]:
                split_at = 2
            else:
                help_text = ''
                for i, chunk in enumerate(name_chunks):
                    help_text += '{} {} '.format(chunk, i+1)
                sys.stdout.write('{}{}'.format(help_text, os.linesep))
                split_at = int(raw_input('Por donde debo picar? '))
            name = ' '.join(name_chunks[split_at:]).strip()
            surname = ' '.join(name_chunks[:split_at]).strip()
            student, created = Student.objects.get_or_create(sid=code, name=name, surname=surname, gender='M')
            if created:
                student.status = StudentStatus.objects.create(year=year, specialty=specialty, student_type=student_type)
                student.save()
            sys.stdout.write('{}{}'.format(student, os.linesep))
